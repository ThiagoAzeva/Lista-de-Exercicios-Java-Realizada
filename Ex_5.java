package LISTA_DE_EXERCÍCIOS_JAVA_BÁSICO;
public class Ex_5 {
public static void main(String[] args) {
int n1 = 125, n2 = 24;
int soma, sub, mult, div, mod;

soma = n1 + n2;
sub = n1 - n2;
mult = n1 * n2;
div = n1 / n2;
mod = n1 % n2;

System.out.println(n1+ "+" +n2+ "=" + soma);

System.out.println(n1+ "-" +n2+ "=" + sub);

System.out.println(n1+ "*" +n2+ "=" + mult);

System.out.println(n1+ "/" +n2+ "=" + div);

System.out.println(n1+ " mod " +n2+ " = " + mod);
	}

}
